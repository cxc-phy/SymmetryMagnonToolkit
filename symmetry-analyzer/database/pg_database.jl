function get_character_table(pgnum)
    syms = pg.get_full_symmetry_operations(pgnum,encode=true)
    character_table = []#zeros(Float64, length(irrep[pgnum]), length(syms))
    for sym in syms
        iso_type = pg.get_isometry_type(sym)
        iso_axis = pg.get_isometry_axis(sym)
        sense_of_rotation = pg.get_sense_of_rotation(sym)
        idx = 0
        for i in eachindex(class[pgnum])
            #println(iso_type)
            #println(-1*iso_axis," ",-1*iso_axis in class[pgnum])
            #println(-1*sense_of_rotation)
            if iso_type == class[pgnum][i][1]
                if iso_axis in class[pgnum][i] && sense_of_rotation in class[pgnum][i][2:end]
                    idx = i
                    break
                elseif -1*iso_axis in class[pgnum][i] && -1*sense_of_rotation in class[pgnum][i][2:end]
                    idx = i
                    break
                end
            end
        end
        println(idx)
        if idx == 0 error("error") end
        for i = 1:length(irrep[pgnum])
            append!(character_table, irrep[pgnum][i][idx])
        end
    end
    character_table = reshape(character_table, length(irrep[pgnum]), length(syms))
    return character_table
end

function output(pgnum, character_table)
    irrepname=keys(irrep[pgnum])
    name=pg.pg_data[pgnum].HM_symbol
    for i = 1:size(character_table, 1)
        str=join([ lpad(string(num),2) for num in character_table[i,:]],", ")
        if i == size(character_table, 1)
            println("    ",rpad(string(irrepname[i]),4),"= (",str,")")
        else
            println("    ",rpad(string(irrepname[i]),4),"= (",str,"),")
        end
    end
    println("),")
end

