"""
A point group operation is compressed using ternary numerical system.
This is achieved because each element of point symmetry matrix can have only one of {-1,0,1}.
For each point-symmetry, there exists a unique 9-digit ternary number corresponding to it.
Then we transform it to the corresponding decimal number.

====Example====
matrix of mirror reflection m_xy: [
                                    0 -1 0
                                    1  0 0
                                    0  0 1
                                            ]
-> vector [0, 1, 0,-1, 0, 0, 0, 0, 1]
-> 9-digit ternary number: 121011112
-> decimal number: 1*3^8 + 2*3^7 + 1*3^6 + 0*3^5 + 1*3^4 + 1*3^3 + 1*3^2 + 1*3^1 + 2*3^0 = 11786
===============


note column major
#row major:rot = [(r%(3^(10-3*(i-1)-j)))÷(3^(9-3*(i-1)-j))-1 for i=1:3,j=1:3]
"""
function pg_encode_symmetry(sym::pg_symtype)::pg_encodetype
    encoded = sum((sym[i]+1)*3^(9-i) for i in eachindex(sym))
    return encoded
end

function pg_decode_symmetry(encoded::pg_encodetype)::pg_symtype
    r = encoded % (3^9)
    rot = reshape([(r%(3^(i)))÷(3^(i-1))-1 for i=9:-1:1], 3, 3)
    return rot
end

